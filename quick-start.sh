#!/bin/bash
# ============================================================================
# تطبيق كرين - سكريبت البدء السريع
# ============================================================================
# هذا السكريبت يعده كل شيء تلقائياً ويبدأ التطبيق

cd "$(dirname "$0")"

echo "════════════════════════════════════════════════════════════"
echo "    تطبيق كرين - سكريبت البدء السريع"
echo "════════════════════════════════════════════════════════════"
echo ""

# ============================================================================
# الخطوة 1: التحقق من المتطلبات
# ============================================================================
echo "🔍 فحص المتطلبات..."

if ! command -v docker &> /dev/null; then
    echo "❌ Docker غير مثبت!"
    exit 1
fi

if ! command -v docker-compose &> /dev/null; then
    echo "❌ Docker Compose غير مثبت!"
    exit 1
fi

echo "✅ Docker و Docker Compose مثبتان"

# ============================================================================
# الخطوة 2: فحص ملف .env
# ============================================================================
echo ""
echo "📋 فحص ملف .env..."

if [ ! -f ".env" ]; then
    echo "⚠️  ملف .env غير موجود!"
    echo "📌 إنشاء ملف .env من .env.example..."
    cp .env.example .env
    echo "✅ تم إنشاء .env"
    echo ""
    echo "⚠️  IMPORTANT: يجب تحديث القيم في .env:"
    echo "   1. TELEGRAM_TOKEN - احصل عليه من @BotFather"
    echo "   2. TELEGRAM_CHAT_ID - احصل عليه من @userinfobot"
    echo "   3. SMTP_USER و SMTP_PASS - بيانات البريد"
    echo ""
    echo "📝 افتح الملف وعدّل:"
    echo "   nano .env"
    exit 1
else
    echo "✅ ملف .env موجود"
fi

# التحقق من Telegram
if ! grep -q "TELEGRAM_TOKEN" .env || [ -z "$(grep '^TELEGRAM_TOKEN' .env | cut -d'=' -f2)" ]; then
    echo "⚠️  تحذير: TELEGRAM_TOKEN فارغ"
fi

if ! grep -q "TELEGRAM_CHAT_ID" .env || [ -z "$(grep '^TELEGRAM_CHAT_ID' .env | cut -d'=' -f2)" ]; then
    echo "⚠️  تحذير: TELEGRAM_CHAT_ID فارغ"
fi

# ============================================================================
# الخطوة 3: فحص الملفات الضرورية
# ============================================================================
echo ""
echo "📂 فحص الملفات الضرورية..."

required_files=(
    "docker-entrypoint.sh"
    "Dockerfile"
    "docker-compose.yml"
    "soc_manger/mine.py"
)

for file in "${required_files[@]}"; do
    if [ ! -f "$file" ]; then
        echo "❌ ملف مفقود: $file"
        exit 1
    fi
done

echo "✅ جميع الملفات الضرورية موجودة"

# ============================================================================
# الخطوة 4: إعطاء الصلاحيات
# ============================================================================
echo ""
echo "🔐 إعطاء الصلاحيات..."

chmod +x docker-entrypoint.sh
echo "✅ تم إعطاء الصلاحيات"

# ============================================================================
# الخطوة 5: حذف الحاويات والصور القديمة
# ============================================================================
echo ""
echo "🗑️  تنظيف الحاويات القديمة..."

docker-compose down 2>/dev/null || true
docker rmi kreen-app 2>/dev/null || true

echo "✅ تم التنظيف"

# ============================================================================
# الخطوة 6: بناء الصورة
# ============================================================================
echo ""
echo "🔨 بناء Docker image..."
echo "   (هذه الخطوة قد تستغرق 2-5 دقائق الأولى)"

if ! docker build -t kreen-app . ; then
    echo "❌ فشل بناء الصورة!"
    exit 1
fi

echo "✅ تم بناء الصورة بنجاح"

# ============================================================================
# الخطوة 7: تشغيل التطبيق
# ============================================================================
echo ""
echo "🚀 بدء التطبيق..."

docker-compose up -d

# الانتظار قليلاً
sleep 3

# فحص حالة الحاويات
echo ""
echo "✅ فحص حالة الخدمات..."
docker-compose ps

# ============================================================================
# الخطوة 8: اختبار الوصول
# ============================================================================
echo ""
echo "🌐 اختبار الوصول..."

max_attempts=10
attempt=1

while [ $attempt -le $max_attempts ]; do
    if curl -s http://127.0.0.1:8001/ > /dev/null; then
        echo "✅ التطبيق يستجيب بنجاح!"
        break
    fi
    echo "⏳ محاولة $attempt/$max_attempts..."
    sleep 1
    attempt=$((attempt + 1))
done

if [ $attempt -gt $max_attempts ]; then
    echo "⚠️  التطبيق لم يستجب بعد"
    echo "   جاري الانتظار... حاول الوصول بعد دقيقة"
fi

# ============================================================================
# ملخص النتائج
# ============================================================================
echo ""
echo "════════════════════════════════════════════════════════════"
echo "    ✅ اكتمل البدء بنجاح!"
echo "════════════════════════════════════════════════════════════"
echo ""
echo "🌐 نقاط الوصول:"
echo "   💻 التطبيق الرئيسي: http://localhost:8001"
echo "   🛡️  جدار الحماية: 127.0.0.1:5000 (داخلي)"
echo ""
echo "📱 الخطوات التالية:"
echo "   1. افتح متصفحك واذهب إلى: http://localhost:8001"
echo "   2. سجل دخول بـ:"
echo "      البريد: admin@kreen.com"
echo "      كلمة المرور: admin123"
echo "   3. افتح Telegram وانتظر التنبيهات من الـ Bot"
echo ""
echo "📊 مراقبة السجلات:"
echo "   docker-compose logs -f"
echo ""
echo "⏹️  لإيقاف التطبيق:"
echo "   docker-compose down"
echo ""
echo "════════════════════════════════════════════════════════════"
